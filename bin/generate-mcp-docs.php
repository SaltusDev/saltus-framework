#!/usr/bin/env php
<?php
/**
 * Generate WordPress-native MCP/Abilities documentation from tool metadata.
 *
 * The generated sections are derived from src/MCP/Tools classes. Edit tool
 * names, descriptions, schemas, routes, and cache behavior in source code,
 * then run `composer docs:mcp`.
 */

declare( strict_types=1 );

use Saltus\WP\Framework\MCP\Tools\RestBackedToolInterface;
use Saltus\WP\Framework\MCP\Tools\ToolInterface;

$root = dirname( __DIR__ );
require_once $root . '/vendor/autoload.php';

if ( ! class_exists( 'WP_REST_Request' ) ) {
	class WP_REST_Request {
		private string $method;
		private string $route;
		/** @var array<string, mixed> */
		private array $params = [];
		/** @var array<string, mixed> */
		private array $body_params = [];

		public function __construct( string $method, string $route ) {
			$this->method = $method;
			$this->route  = $route;
		}

		public function set_param( string $key, mixed $value ): void {
			$this->params[ $key ] = $value;
		}

		/**
		 * @param array<string, mixed> $params
		 */
		public function set_body_params( array $params ): void {
			$this->body_params = $params;
		}

		public function get_method(): string {
			return $this->method;
		}

		public function get_route(): string {
			return $this->route;
		}

		/**
		 * @return array<string, mixed>
		 */
		public function get_params(): array {
			return $this->params;
		}

		/**
		 * @return array<string, mixed>
		 */
		public function get_body_params(): array {
			return $this->body_params;
		}
	}
}

$tools = collect_tools( $root . '/src/MCP/Tools' );
usort(
	$tools,
	static function ( ToolInterface $a, ToolInterface $b ): int {
		return strcmp( $a->get_name(), $b->get_name() );
	}
);

$reference = build_reference_document( $tools );
write_file_if_changed( $root . '/docs/MCP-ABILITIES.md', $reference );
replace_generated_section(
	$root . '/docs/MCP.md',
	'<!-- BEGIN AUTO-GENERATED MCP ABILITIES -->',
	'<!-- END AUTO-GENERATED MCP ABILITIES -->',
	build_embedded_section( $tools )
);

echo 'Generated MCP ability docs for ' . count( $tools ) . " tools.\n";

/**
 * @return list<ToolInterface>
 */
function collect_tools( string $tool_dir ): array {
	$files = glob( $tool_dir . '/*.php' );
	if ( $files === false ) {
		return [];
	}

	sort( $files );
	$tools = [];

	foreach ( $files as $file ) {
		$class = 'Saltus\\WP\\Framework\\MCP\\Tools\\' . basename( $file, '.php' );
		if ( ! class_exists( $class ) ) {
			continue;
		}

		$reflection = new ReflectionClass( $class );
		if ( ! $reflection->isInstantiable() || ! $reflection->implementsInterface( ToolInterface::class ) ) {
			continue;
		}

		$constructor = $reflection->getConstructor();
		if ( $constructor !== null && $constructor->getNumberOfRequiredParameters() > 0 ) {
			continue;
		}

		$tool = $reflection->newInstance();
		if ( $tool instanceof ToolInterface ) {
			$tools[] = $tool;
		}
	}

	return $tools;
}

/**
 * @param list<ToolInterface> $tools
 */
function build_reference_document( array $tools ): string {
	$lines = [
		'# MCP Ability Reference',
		'',
		'<!-- This file is auto-generated by `composer docs:mcp`. Do not edit by hand. -->',
		'',
		'Saltus Framework exposes ' . count( $tools ) . ' WordPress-native MCP/Abilities tools.',
		'',
		build_table( $tools ),
		'',
	];

	foreach ( $tools as $tool ) {
		$metadata = tool_metadata( $tool );
		$lines[] = '## `' . $tool->get_name() . '`';
		$lines[] = '';
		$lines[] = $tool->get_description();
		$lines[] = '';
		$lines[] = '- Ability: `' . $metadata['ability'] . '`';
		$lines[] = '- REST request: `' . $metadata['rest_request'] . '`';
		$lines[] = '- REST capability: `' . $metadata['capability'] . '`';
		$lines[] = '- Cacheable: `' . $metadata['cacheable'] . '`';
		$lines[] = '- Cache TTL: `' . $metadata['cache_ttl'] . '`';
		$lines[] = '';
		$lines[] = '### Parameters';
		$lines[] = '';
		$lines[] = parameters_table( $tool->get_parameters() );
		$lines[] = '';
	}

	return implode( "\n", $lines );
}

/**
 * @param list<ToolInterface> $tools
 */
function build_embedded_section( array $tools ): string {
	return implode(
		"\n",
		[
			'<!-- This section is auto-generated by `composer docs:mcp`. Do not edit by hand. -->',
			'',
			build_table( $tools ),
			'',
			'For full generated parameter details, see [MCP-ABILITIES.md](MCP-ABILITIES.md).',
		]
	);
}

/**
 * @param list<ToolInterface> $tools
 */
function build_table( array $tools ): string {
	$lines = [
		'| Tool | Ability | REST request | Description |',
		'|------|---------|--------------|-------------|',
	];

	foreach ( $tools as $tool ) {
		$metadata = tool_metadata( $tool );
		$lines[]  = '| `' . esc_md( $tool->get_name() ) . '` | `' . esc_md( $metadata['ability'] ) . '` | `' . esc_md( $metadata['rest_request'] ) . '` | ' . esc_md( $tool->get_description() ) . ' |';
	}

	return implode( "\n", $lines );
}

/**
 * @return array{ability: string, rest_request: string, capability: string, cacheable: string, cache_ttl: string}
 */
function tool_metadata( ToolInterface $tool ): array {
	$request = null;
	if ( $tool instanceof RestBackedToolInterface ) {
		$request = $tool->build_rest_request( sample_args( $tool->get_parameters() ) );
	}

	$rest_request = 'n/a';
	if ( $request instanceof WP_REST_Request ) {
		$route        = normalize_route_placeholders( $request->get_route() );
		$rest_request = $request->get_method() . ' ' . $route;
	}

	$capability = 'none';
	if ( $tool instanceof RestBackedToolInterface && $tool->get_rest_capability() !== null ) {
		$requirement = $tool->get_rest_capability();
		$capability  = $requirement->get_capability();
		if ( $requirement->get_model_type() !== null ) {
			$capability .= ' (' . $requirement->get_model_type() . ')';
		}
	}

	return [
		'ability'      => ability_name( $tool->get_name() ),
		'rest_request' => $rest_request,
		'capability'   => $capability,
		'cacheable'    => $tool instanceof RestBackedToolInterface && $tool->is_cacheable() ? 'yes' : 'no',
		'cache_ttl'    => $tool instanceof RestBackedToolInterface && $tool->is_cacheable() ? (string) $tool->cache_ttl() . 's' : 'n/a',
	];
}

/**
 * @param array<string, mixed> $schema
 * @return array<string, mixed>
 */
function sample_args( array $schema ): array {
	$args = [];

	foreach ( $schema as $name => $definition ) {
		if ( ! is_array( $definition ) ) {
			continue;
		}

		if ( array_key_exists( 'default', $definition ) ) {
			$args[ $name ] = $definition['default'];
			continue;
		}

		$args[ $name ] = placeholder_value( (string) $name, (string) ( $definition['type'] ?? 'string' ) );
	}

	return $args;
}

function placeholder_value( string $name, string $type ) {
	$values = [
		'post_id'   => 123,
		'post_type' => '{post_type}',
		'taxonomy'  => '{taxonomy_rest_base}',
		'settings'  => [],
		'items'     => [],
		'title'     => '{title}',
		'name'      => '{name}',
	];

	if ( array_key_exists( $name, $values ) ) {
		return $values[ $name ];
	}

	if ( $type === 'number' || $type === 'integer' ) {
		return 1;
	}

	if ( $type === 'boolean' ) {
		return true;
	}

	if ( $type === 'array' ) {
		return [];
	}

	if ( $type === 'object' ) {
		return [];
	}

	return '{' . $name . '}';
}

function normalize_route_placeholders( string $route ): string {
	$replacements = [
		'%7Bpost_type%7D'          => '{post_type}',
		'%7Btaxonomy_rest_base%7D' => '{taxonomy_rest_base}',
		'%7Bname%7D'               => '{name}',
		'%7Bslug%7D'               => '{slug}',
	];

	return str_replace( array_keys( $replacements ), array_values( $replacements ), $route );
}

/**
 * @param array<string, mixed> $parameters
 */
function parameters_table( array $parameters ): string {
	if ( $parameters === [] ) {
		return 'This tool does not accept parameters.';
	}

	$lines = [
		'| Parameter | Type | Required | Default | Description |',
		'|-----------|------|----------|---------|-------------|',
	];

	foreach ( $parameters as $name => $definition ) {
		$definition  = is_array( $definition ) ? $definition : [];
		$type        = isset( $definition['type'] ) ? (string) $definition['type'] : 'mixed';
		$required    = ! empty( $definition['required'] ) ? 'yes' : 'no';
		$default     = array_key_exists( 'default', $definition ) ? code_value( $definition['default'] ) : '';
		$description = isset( $definition['description'] ) ? (string) $definition['description'] : '';
		$lines[]     = '| `' . esc_md( (string) $name ) . '` | `' . esc_md( $type ) . '` | ' . $required . ' | ' . esc_md( $default ) . ' | ' . esc_md( $description ) . ' |';
	}

	return implode( "\n", $lines );
}

function ability_name( string $tool_name ): string {
	return strtolower( 'saltus/' . str_replace( '_', '-', $tool_name ) );
}

function code_value( $value ): string {
	if ( is_bool( $value ) ) {
		return $value ? '`true`' : '`false`';
	}

	if ( is_array( $value ) ) {
		return '`[]`';
	}

	if ( $value === null ) {
		return '`null`';
	}

	return '`' . (string) $value . '`';
}

function esc_md( string $value ): string {
	return str_replace( '|', '\\|', $value );
}

function replace_generated_section( string $path, string $start, string $end, string $replacement ): void {
	$content = file_get_contents( $path );
	if ( $content === false ) {
		throw new RuntimeException( 'Unable to read ' . $path );
	}

	$start_pos = strpos( $content, $start );
	$end_pos   = strpos( $content, $end );

	if ( $start_pos === false || $end_pos === false || $end_pos <= $start_pos ) {
		throw new RuntimeException( 'Generated section markers not found in ' . $path );
	}

	$new_content = substr( $content, 0, $start_pos + strlen( $start ) )
		. "\n"
		. $replacement
		. "\n"
		. substr( $content, $end_pos );

	write_file_if_changed( $path, $new_content );
}

function write_file_if_changed( string $path, string $content ): void {
	if ( file_exists( $path ) && file_get_contents( $path ) === $content ) {
		return;
	}

	if ( file_put_contents( $path, $content ) === false ) {
		throw new RuntimeException( 'Unable to write ' . $path );
	}
}
